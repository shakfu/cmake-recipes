def title(name):
    space = '-'*79
    return f'\nmessage("\n{name}")\nmessage("{space}")'

def dump_var(var):
    return f'message("{var}: " ${{{var}}})'

def dump_vars(variables, name, file=None):
    message = lambda v: f'message("{v}: " ${{{v}}})'

    print(title(name), file=file)
    for line in variables.splitlines():
        if '<' in line: # skips variable variables
            continue
        var = line.strip()
        print(dump_var(var), file=file)

def dump_cmake_vars(filename=None):
    if filename:
        with open(filename, 'w') as f:
            dump_vars(cmake_info_vars,    'cmake_info_vars',   file=f)
            dump_vars(cmake_change_vars,  'cmake_change_vars', file=f)
            dump_vars(cmake_sys_vars,     'cmake_sys_vars',    file=f)
            dump_vars(cmake_lang_vars,    'cmake_lang_vars',   file=f)

if __name__ == '__main__':
    dump_cmake_vars('dump_vars.cmake')


